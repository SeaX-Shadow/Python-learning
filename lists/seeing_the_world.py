# Places I want to visit
places = ['Maldives', 'Paris', 'America', 'Bosnia', 'Slovakia']
print(places)
# Sorting the list temporarily in alphabetical order using 'sorted'
sorted(places)
print(places)
# Printing the list to make sure it is the same
print(places)
# Reverse alphabetical with sorted
sorted(places, reverse=True)
print(places)
#Reprinting to show it's in the original form
print(places)
# Reversing the order of the list
places.reverse()
print(places)
# Printing to show that the list has permantly changed
print(places)
#Changing the order again
places.reverse()
print(places)
# Alphabetical order 
places.sort()
print(places)
# Reverse order
places.sort(reverse=True)
print(places)